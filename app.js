function disableEnableElements(t) {

    const divv = document.getElementById('nickname_generator');

    if (t === 0) {
        divv.classList.remove('hidden-elements');
        $genero_selection.classList.add('hidden-elements');
    } else {
        divv.classList.add('hidden-elements');
        $genero_selection.classList.remove('hidden-elements');
    }

}